    private void cariData(String cari){
        String sqlCari="";
        try {
            if (cari.isEmpty()){
                sqlCari="SELECT*FROM t_hutanrakyat";
            }else {
                sqlCari="SELECT*FROM t_hutanrakyat where id_hutanrakyat='"+cari+"'"+
                        " or luas='"+cari+"'"+
                        " or longitude='"+cari+"'"+
                        " or latitude='"+cari+"'";
            }
            crud.settingJudulTabel(tableHutan, JudulKolom);
            crud.tampilTabel(tableHutan, sqlCari, JudulKolom);
            crud.settingLebarKolom(tableHutan, LebarKolom);
        }catch (Exception e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }
    

