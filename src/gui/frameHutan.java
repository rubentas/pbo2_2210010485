    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            if(crud.DuplicateKey("t_hutanrakyat", "id_hutanrakyat", txtId.getText())){
                JOptionPane.showMessageDialog(null, "ID Hutan sudah ada");
            }else{
                String[] isiField = {txtId.getText(), txtLuas.getText(),txtLong.getText(), txtLat.getText()};
                crud.simpanDinamis("t_hutanrakyat", fieldSimpan, isiField);
                JOptionPane.showMessageDialog(null, "Berhasil disimpan");
                refreshHutan();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

