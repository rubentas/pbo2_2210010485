    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            if(crud.DuplicateKey("t_klasifikasi", "id_klasifikasi", txtId.getText())){
                JOptionPane.showMessageDialog(null, "Kode ID KLASIFIKASI sudah ada");
            }else{
                String[] isiField = {txtId.getText(), txtJenis.getText(),
                txtMarker.getText()};
                crud.simpanDinamis("t_klasifikasi", fieldSimpan, isiField);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.toString());
        }
        refreshKlasifikasi();
    }//GEN-LAST:event_jButton1ActionPerformed

